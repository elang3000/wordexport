package com.imooc.eurekaclient;

public class SellPlan {
	private String businessYear;
	private String liveYear;
	private String leader;
	private String phone;
	private String curYear;
	private String positionImage;
	public String getBusinessYear() {
		return businessYear;
	}
	public void setBusinessYear(String businessYear) {
		this.businessYear = businessYear;
	}
	public String getLiveYear() {
		return liveYear;
	}
	public void setLiveYear(String liveYear) {
		this.liveYear = liveYear;
	}
	public String getLeader() {
		return leader;
	}
	public void setLeader(String leader) {
		this.leader = leader;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getCurYear() {
		return curYear;
	}
	public void setCurYear(String curYear) {
		this.curYear = curYear;
	}
	public String getPositionImage() {
		return positionImage;
	}
	public void setPositionImage(String positionImage) {
		this.positionImage = positionImage;
	}
}
